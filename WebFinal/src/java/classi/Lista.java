public class Lista {
    
    private int utente;
    private int gruppo;
    private int invitato;
    private int ID;

    public int getInvitato() {
        return invitato;
    }

    public void setInvitato(int invitato) {
        this.invitato = invitato;
    }

    public int getUtente() {
        return utente;
    }

    public void setUtente(int utente) {
        this.utente = utente;
    }

    public int getGruppo() {
        return gruppo;
    }

    public void setGruppo(int gruppo) {
        this.gruppo = gruppo;
    }

    public int getID() {
        return ID;
    }

    public void setID(int ID) {
        this.ID = ID;
    }

}
