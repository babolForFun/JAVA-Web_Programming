public class Invito {
    private int utente;
    private int gruppo;
    private int flag_invito;

    public int getUtente() {
        return utente;
    }

    public void setUtente(int utente) {
        this.utente = utente;
    }

    public int getGruppo() {
        return gruppo;
    }

    public void setGruppo(int gruppo) {
        this.gruppo = gruppo;
    }

    public int getFlag_invito() {
        return flag_invito;
    }

    public void setFlag_invito(int flag_invito) {
        this.flag_invito = flag_invito;
    }
    
}
